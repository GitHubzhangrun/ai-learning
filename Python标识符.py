# 标识符：用户在编程的时候所使用的一系列名字，用来给变量/类/方法等命名
# 命名规则————


# 内容限定： 限定只能使用中/英/数字/下划线，不能以数字开头
# 错误命名例子：1_name = "张三"
# 错误命名例子：name_! = "张三"
name_ = "张三"
_name = "张三"
name_1 = "张三"

# 大小写敏感：
Itheima = "黑马程序员"
itheima = 666
print(Itheima,itheima)

# 不可使用关键字：False True None等不用刻意去背
# 使用了关键字：class = 1
Class = 2

# 变量命名规范：见名知意/下划线串联/英文字母全小写